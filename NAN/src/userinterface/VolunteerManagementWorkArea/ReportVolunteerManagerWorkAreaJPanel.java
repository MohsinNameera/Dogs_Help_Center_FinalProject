    private void populateStatusTable() {
       
        statusTable.getTableHeader().setDefaultRenderer(new HeaderColors());
        DefaultTableModel model = (DefaultTableModel) statusTable.getModel();
        model.setRowCount(0);
        for (WorkRequest wr : account.getWorkQueue().getWorkRequestList()) {            
            if(wr instanceof RescueUnitRequest && null != selectedWorkReq) {
                
                if(selectedWorkReq.getSceneId().equals(((RescueUnitRequest) wr).getSceneId())) {
                    Object[] row = new Object[model.getColumnCount()];                
                    row[0] = ((RescueUnitRequest) wr);
                    row[1] = ((RescueUnitRequest) wr).getStatus();
                    row[2] = ((RescueUnitRequest) wr).getRecieverOrganization().getName();
                    row[3] = ((RescueUnitRequest) wr).getRequestDate();
                    row[4] = ((RescueUnitRequest) wr).getSender().getEmployee().getName();
                    row[5] = ((RescueUnitRequest) wr).getMessage();
                    row[6] = ((RescueUnitRequest) wr).getSceneId();
                    row[7] = ((RescueUnitRequest) wr).getRecieverNetwork().getName();
                    model.addRow(row);
                }
            }            
        }
    
    }

    private void populateSceneTable() {
        sceneTable.getTableHeader().setDefaultRenderer(new HeaderColors());
        DefaultTableModel model = (DefaultTableModel) sceneTable.getModel();
        model.setRowCount(0);
        
        for (WorkRequest wr : account.getWorkQueue().getWorkRequestList()) {
            if(wr instanceof RescueAdminReportRequest) {
                Object[] row = new Object[model.getColumnCount()];
                row[0] = ((RescueAdminReportRequest) wr);
                row[1] = ((RescueAdminReportRequest) wr).getSceneName();
                row[2] = ((RescueAdminReportRequest) wr).getNoOfVictims();
                row[3] = ((RescueAdminReportRequest) wr).getSceneZipcode();
                row[4] = ((RescueAdminReportRequest) wr).getSender();
                row[5] = ((RescueAdminReportRequest) wr).getStatus();
                row[6] = ((RescueAdminReportRequest) wr).getRequestDate();
                row[7] = ((RescueAdminReportRequest) wr).getMessage();
                model.addRow(row);
            }
        }
       
    }

