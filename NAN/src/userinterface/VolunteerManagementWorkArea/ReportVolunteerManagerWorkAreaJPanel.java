    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int selectedRow = sceneTable.getSelectedRow();        
        RescueAdminReportRequest selectedScene = null;
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a Report", "Warning", JOptionPane.WARNING_MESSAGE);
        }else {
            boolean isReqCompleted = true;
            for (WorkRequest wr : account.getWorkQueue().getWorkRequestList()) {            
                if(wr instanceof RescueUnitRequest && null != selectedWorkReq) {
                    if(selectedWorkReq.getSceneId().equals(((RescueUnitRequest) wr).getSceneId())) {
                        if(!((RescueUnitRequest)wr).getStatus().equals("Completed") && !((RescueUnitRequest)wr).getStatus().equals("Cancelled") && !((RescueUnitRequest)wr).getStatus().equals("Rejected")) {
                            isReqCompleted = false;
                            break;
                        }
                    }
                }            
            }
            
            if(isReqCompleted) {
                RescueAdminReportRequest sScene = (RescueAdminReportRequest) sceneTable.getValueAt(selectedRow, 0);
                if(sScene.getStatus().equals("Resolved")) {
                    JOptionPane.showMessageDialog(null, "Report is already Resolved");
                }else if(sScene.getStatus().equals("Volunteer Manager Assigned")) {
                    JOptionPane.showMessageDialog(null, "Please process the Report before marking resolved");
                }else {
                    String msg = JOptionPane.showInputDialog("Additional Message");                    
                    sScene.setStatus("Resolved");
                    sScene.setMessage(msg);
                }
                
            }else {
                JOptionPane.showMessageDialog(null, "There are pending requests for this Report. Please wait for them to be completed before marking resolved.");
            }
            
        }
    }//GEN-LAST:event_jButton3ActionPerformed

