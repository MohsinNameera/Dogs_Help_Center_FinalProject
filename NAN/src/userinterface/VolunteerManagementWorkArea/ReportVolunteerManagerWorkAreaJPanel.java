    private void nearestOrgSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nearestOrgSearchActionPerformed
        // TODO add your handling code here:
        int selectedRow = sceneTable.getSelectedRow();        
        RescueAdminReportRequest selectedScene = null;
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select the row to view available organizations", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            selectedScene = (RescueAdminReportRequest) sceneTable.getValueAt(selectedRow, 0);
           
            if(selectedScene.getStatus().equals("Resolved")) {
                JOptionPane.showMessageDialog(null, "Report is already Resolved");
            }else if(selectedScene.getStatus().equals("Volunteer Manager Assigned")) {
                JOptionPane.showMessageDialog(null, "Please process the Report before sending requests to organizations");
            }else {
                AvailableOrganizationJPanel ramop = new AvailableOrganizationJPanel(userProcessContainer, account, organization, selectedScene, enterprise, network, business);
                userProcessContainer.add("Available_ORG", ramop);
                CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                layout.next(userProcessContainer);
            }
        }
    }//GEN-LAST:event_nearestOrgSearchActionPerformed

